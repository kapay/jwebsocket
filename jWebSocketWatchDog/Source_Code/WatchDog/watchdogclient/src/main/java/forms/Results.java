     private void initTableTest() {
        //Setting the name of the columns of the Test table
        //testId=1 Test, testResult=false, fatal=false
        String[] columnsnamestest = new String[]{"id", "Result", "Fatal"};
        mModelTest = new DefaultTableModel(columnsnamestest, 0);
        jTable1.setModel(mModelTest);
        jTable1.setCellSelectionEnabled(true);
    }
     
     private void loadTestTable() {

        initTableTest();
       
         for (int i = 0; i < mReport.getReport().size(); i++) {
             mModelTest.addRow(new String[]{mReport.getReport().get(i).getTestId().toString(), mReport.getReport().get(i).getTestResult().toString(), mReport.getReport().get(i).isFatal().toString()});
             if ((!mReport.getReport().get(i).getTestResult()&&(mReport.getReport().get(i).isFatal())) ) {
                ApplicationContext ctx = new FileSystemXmlApplicationContext("conf/config.xml");
                MailNotifier mailer = ((MailNotifier)ctx.getBean("Mailer"));
                mailer.notify("The task" + mReport.getReport().get(i).getTestId() + " has failed. Please check the server inmediately!");
             }
         }
    }
     
    

